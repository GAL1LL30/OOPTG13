package E1_KNearestNeighbour;

public class DataObjekt {
    int x;
    int y;
    int klasse;
    
    public DataObjekt(int x, int y, int klasse)
    {
        this.x = x;
        this.y = y;
        this.klasse = klasse;
    }

    public DataObjekt (int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}
